    private void jButtonComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComprarActionPerformed
        Acoes ti = new Acoes();
        float val1;
        float val;

        val = user.getCarteira().getSaldo() - user.getCarteira().getSaldoInvestido();
        float valor = this.calcularValor();

        if (valor <= val) {
            if (jTextTrigger.getText().isEmpty()) {
                val1 = 0;
            } else {
                val1 = Float.parseFloat(jTextTrigger.getText());
            }

            user.getCarteira().setSaldoInvestido(user.getCarteira().getSaldoInvestido() + valor);
            ti = Ficheiro.getRepositorio().getAcao().get(this.jComboBox1.getSelectedItem().toString());
            user.getCarteira().getHistorico().put(new Oferta(user, Integer.parseInt(this.jTextQtd.getText()), Float.parseFloat(this.jTextPreco.getText()), ti), 1);
            ti.criarOfertaCompra(user, Integer.parseInt(this.jTextQtd.getText()), Float.parseFloat(this.jTextPreco.getText()), ti, this.jRadioButton1.isSelected(), val1, true);
            JOptionPane.showMessageDialog(null, "Compra efetuada!");
        } else {
            System.out.println("e o dinheiro?");
            JOptionPane.showMessageDialog(null, "Não tem dinheiro suficiente!");
            
        }

    }//GEN-LAST:event_jButtonComprarActionPerformed

