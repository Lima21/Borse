    private void jRBbuyItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jRBbuyItemStateChanged
        if (this.jRBbuy.isSelected() && !Ficheiro.getRepositorio().getAcao().get(this.jComboBox1.getSelectedItem().toString()).getOfertaVenda().isEmpty()) {
            String x = String.valueOf(Ficheiro.getRepositorio().getAcao().get(this.jComboBox1.getSelectedItem().toString()).getOfertaVenda().peek().getPreco());
            
            this.jTextPreco.setText(x);
            int qtd = Integer.parseInt(this.jTextQtd.getText());
            if (qtd > Ficheiro.getRepositorio().getAcao().get(this.jComboBox1.getSelectedItem().toString()).getOfertaVenda().peek().getQuantidade()) {
               // JOptionPane.showMessageDialog(null, "Quantidade excedida");
                this.jTextQtd.setText(String.valueOf(Ficheiro.getRepositorio().getAcao().get(this.jComboBox1.getSelectedItem().toString()).getOfertaVenda().peek().getQuantidade()));
            }
            this.jTextQtd.setEnabled(false);
            this.jTextPreco.setEnabled(false);

        } else {
            this.jTextQtd.setEnabled(true);
            this.jTextPreco.setEnabled(true);
            this.jRBbuy.setSelected(false);
        }
    }//GEN-LAST:event_jRBbuyItemStateChanged

    private float calcularValor() {
        int qta;
        float valor;
        System.out.println("ola");
        try {
            qta = Integer.parseInt(this.jTextQtd.getText());
        } catch (NumberFormatException ex) {
            qta = 0;
            System.out.println("erro: " + ex);
        }

        try {
            valor = Float.parseFloat(this.jTextPreco.getText());
        } catch (NumberFormatException ex) {
            valor = 0;
            System.out.println("erro: " + ex);
        }

        valor *= qta;

        this.jLabelValor.setText(Float.toString(valor));
        return valor;
    }

